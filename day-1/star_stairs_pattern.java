public class star_stairs_pattern{
    public static void main(String[] args){
        //code for star pattern
        for(int i=1;i<=4;i++){
            for(int j=1;j<=i;j++){
                System.out.print("* ");
            }
            System.out.println("");
            

        }
        //star pattern by manual representation code
        System.out.println("* \n* * \n* * * \n* * * *");
    }

}